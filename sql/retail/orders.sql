-- Table: orders

CREATE TABLE retail_schema.orders (
    order_id INT PRIMARY KEY,
    customer_id INT,
    product_id INT,
    quantity INT,
    order_date DATE,
    FOREIGN KEY (customer_id) REFERENCES retail_schema.customers(customer_id),
    FOREIGN KEY (product_id) REFERENCES retail_schema.products(product_id)
);

